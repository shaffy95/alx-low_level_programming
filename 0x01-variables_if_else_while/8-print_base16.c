#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
* main - determine if a number is positive, negative or zero.
* Return: Always 0 (success)
*/
int main(void)
{
	int n;
	char ch;

	for (n = 0; n < 10; n++)
	{
		putchar(n);
	}
	for (ch = 'a'; ch >= 'f'; ch++)
	{
		putchar(ch);
	}

	putchar('\n');
	return (0);
}
